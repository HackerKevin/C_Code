#define _CRT_SECURE_NO_WARNINGS	1
#include <stdio.h>
int main()
{
	char a = -1;
	//原码：10000000000000000000000000000001
	//补码：11111111111111111111111111111111
	//截断
	//11111111 - a
	signed char b = -1;
	//原码：10000000000000000000000000000001
	//补码：11111111111111111111111111111111
	//截断
	//11111111 - b
	unsigned char c = -1;
	//原码：10000000000000000000000000000001
	//补码：11111111111111111111111111111111
	//截断
	//11111111 - c
	printf("a=%d,b=%d,c=%d", a, b, c);
	//%d - 以十进制的形式打印有符号的整型
	//整型提升
	//a - 11111111111111111111111111111111 - 补码 - 原码：-1
	//b - 11111111111111111111111111111111 - 补码 - 原码：-1
	//c - 00000000000000000000000011111111 - 补码 - 原码：255
	return 0;
}
