#define _CRT_SECURE_NO_WARNINGS	1
#include <stdio.h>
int main()
{
	//有符号char类型的取值范围：-128~127
	char a = -128;
	//原码：10000000000000000000000100000000
	//反码：11111111111111111111111011111111
	//补码：11111111111111111111111100000000
	//截断
	//100000000 - a
	char b = 128;
	//原码：00000000000000000000000100000000
	//补码：00000000000000000000000100000000
	//截断
	//100000000 - b
	printf("%u\n", a);
	printf("%u\n", b);
	//%u - 以十进制的形式打印无符号的整型
	//整型提升
	//a - 11111111111111111111111100000000
	//b - 11111111111111111111111100000000
	//提升后以十进制的形式打印无符号的整型
	//a - 11111111111111111111111100000000 - 补码 - 原码：4294967040
	//b - 11111111111111111111111100000000 - 补码 - 原码：4294967040
	return 0;
}
